function getLocation($data){
    $location = $data->street . ' ' . $data->street_number;
    $location .= ", " . $data->city;
    if ($data->country !== null) {
        $location .= ", " . $data->country;
    }
    
    return $location;
}

function businessPicture($business)
{
    return url('business') . '/' . $business['id'] . '/50.50.' . $business['picture'] . '?v=' . rand(1111, 99999);
}

function setLocationURL($type, $param)
{
    $url = url('/') . '/map/';
    $delimiter = '/';
    switch ($type) {
        case 'country':
            $url .= 'country/' . $param->country;
            break;
        case 'city':
            $url .= 'city/' . $param->city . $delimiter . $param->country;
            break;
        case 'region':
            $url .= 'region/' . $param->region . $delimiter . $param->country;
            break;
        case 'street':
            $url .= 'street/' . $param->street . $delimiter . $param->city . $delimiter . $param->country;
            break;
        case 'address':
            $url .= 'address/' . $param->street_number . '+' . $param->street . $delimiter . $param->city . $delimiter . $param->country;
            break;
    }
    
    return $url;
}

function getDisplayingTitle($type)
{
    switch ($type) {
        case 'jobs':
            return trans('main.label.jobs');
            break;
        case 'headquarters':
            return trans('main.label.headquarters');
            break;
        case 'locations':
            return trans('main.label.branches');
            break;
        case 'nearby-jobs':
            return 'Jobs';
            break;
        case 'nearby-locations':
            return 'Locations';
            break;
        case 'employers':
            return 'Employers';
            break;
        case 'keywords':
            return 'Keywords';
            break;
    }
}

function isShowItemFromIntegrationGuide ($itemNumber, $itemNumberOr =null)
{
    $business = \App\Business::find(Illuminate\Support\Facades\Cookie::get('business-id'));
    if (!$business || ($business && !$business->lets_get_started)) {
        return true;
    }
    $data = explode(',', $business->lets_get_started);
    if ($data[$itemNumber] == 1 || ($itemNumberOr && $data[$itemNumberOr] == 1)) {
        return true;
    }

    return false;
}
